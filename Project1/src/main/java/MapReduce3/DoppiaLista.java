package MapReduce3;

public class DoppiaLista <u1,u2>
{
	private u1 primo;
	private u2 secondo;
	
	public DoppiaLista(u1 primo, u2 secondo) {
		this.primo = primo;
		this.secondo = secondo;
	}
	public u1 getPrimo() {
		return primo;
	}
	public void setPrimo(u1 primo) {
		this.primo = primo;
	}
	public u2 getSecondo() {
		return secondo;
	}
	public void setSecondo(u2 secondo) {
		this.secondo = secondo;
	}
	

}
